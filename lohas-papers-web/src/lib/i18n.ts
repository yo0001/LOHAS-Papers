export function t(locale: Locale, key: string, replacements?: Record<string, string | number>): string {
  const value = translations[locale]?.[key] ?? translations.en[key] ?? key;
  if (!replacements) return value;
  return Object.entries(replacements).reduce(
    (str, [k, v]) => str.replace(`{${k}}`, String(v)),
    value
  );
}

export function getBasedOnPapers(locale: Locale, n: number): string {
  const unit = t(locale, "basedOnPapers");
  if (locale === "ja") return `${n}${unit}`;
  if (locale === "ko") return `${n}${unit}`;
  if (locale === "zh-Hans") return `基于${n}${unit}`;
  if (locale === "th") return `อ้างอิงจาก ${n} ${unit}`;
  return `Based on ${n} ${unit}`;
}
